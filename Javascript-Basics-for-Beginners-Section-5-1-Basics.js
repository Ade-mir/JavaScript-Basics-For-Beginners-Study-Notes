// Object Oriented Programming (OOP)

const circle = {
  radius: 1,
  location: {
    x: 1,
    y: 1,
  },
  isVisible: true,
  draw: function () {
    console.log('draw');
  },
};

circle.draw(); // Calling a function which is saved as a "Key value pair" inside of an object means you are calling a Method.
